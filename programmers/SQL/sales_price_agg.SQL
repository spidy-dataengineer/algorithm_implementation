SELECT A.AUTHOR_ID,
       B.AUTHOR_NAME,
       A.CATEGORY,
       sum(C.SALES * A.PRICE) AS TOTAL_SALES
FROM BOOK A
INNER JOIN AUTHOR B
ON A.AUTHOR_ID = B.AUTHOR_ID
INNER JOIN (SELECT *
            FROM BOOK_SALES
            WHERE SALES_DATE BETWEEN '2022-01-01' and '2022-01-31'
            ) C
ON A.BOOK_ID = C.BOOK_ID
GROUP BY A.AUTHOR_ID,A.CATEGORY
ORDER BY A.AUTHOR_ID,A.CATEGORY DESC